/* Task to control pump */
void vTaskPumpControl(void *pvParameters)
{
  for (;;)
  {
    /* Check if humidity is less than threshold */
     // kiem tra nguong do am va bat tat relay
    if (humidity < THRESHOLD) {
     
			HAL_GPIO_WritePin(RELAY_PORT, RELAY_PIN, GPIO_PIN_SET);
			 
			// Hien thi trang thai bom
      CLCD_I2C_SetCursor(&LCD1, 0, 1);
      CLCD_I2C_WriteString(&LCD1, "BOM NUOC: ON ");
    } else {
      
			HAL_GPIO_WritePin(RELAY_PORT, RELAY_PIN, GPIO_PIN_RESET);
		
			// Hien thi trang thai dung bom
      CLCD_I2C_SetCursor(&LCD1, 0, 1);
      CLCD_I2C_WriteString(&LCD1, "BOM NUOC: OFF ");
			
    }

    /* Delay for 1 seconds */
    vTaskDelay(pdMS_TO_TICKS(1000));
  }
}


